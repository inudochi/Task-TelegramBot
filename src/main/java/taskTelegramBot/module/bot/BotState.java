package taskTelegramBot.module.bot;

/**
 * Перечисление состояний пользователя в Telegram боте
 *
 * <p>Определяет все возможные состояния, в которых может находиться
 * пользователь во время взаимодействия с ботом. Каждое состояние
 * соответствует определенному этапу диалога или операции.</p>
 *
 * @author Чекрыгин И.И.
 * @version 1.0
 */
public enum BotState {
    /** Начальное состояние при первом запуске бота */
    START,

    /** Выбор источника данных (JSON или PostgreSQL) */
    CHOOSING_SOURCE,

    /** Главное меню с основными операциями */
    MAIN_MENU,

    /** Ввод названия новой игры */
    ADDING_GAME_NAME,

    /** Выбор жанра новой игры */
    ADDING_GAME_GENRE,

    /** Ввод минимального количества игроков */
    ADDING_GAME_MIN_PLAYERS,

    /** Ввод максимального количества игроков */
    ADDING_GAME_MAX_PLAYERS,

    /** Выбор игры для удаления */
    DELETING_GAME_SELECT,

    /** Подтверждение удаления выбранной игры */
    DELETING_GAME_CONFIRM,

    /** Выбор игры для редактирования */
    EDITING_GAME_SELECT,

    /** Редактирование конкретного поля выбранной игры */
    EDITING_GAME_FIELD,

    /** Выбор игры для планирования сессии */
    PLANNING_SESSION_SELECT,

    /** Ввод даты игровой сессии */
    PLANNING_SESSION_DATE,

    /** Ввод времени игровой сессии */
    PLANNING_SESSION_TIME
}