package taskTelegramBot.module.dao;

/**
 * Интерфейс для операций записи данных об играх
 *
 * <p>Определяет контракт для классов, отвечающих за сохранение,
 * обновление и удаление информации об играх в различных источниках данных.</p>
 *
 * @author Чекрыгин И.И.
 * @version 1.0
 */
public interface GameWriter {

    /**
     * Добавляет новую игру в коллекцию
     *
     * @param game игра для добавления
     */
    void addGame(Game game);

    /**
     * Обновляет информацию об существующей игре
     *
     * @param game игра с обновленными данными
     */
    void updateGame(Game game);

    /**
     * Удаляет игру из коллекции по идентификатору
     *
     * @param id идентификатор игры для удаления
     */
    void deleteGame(int id);
}