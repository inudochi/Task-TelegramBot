package taskTelegramBot.module.dao;

import java.util.List;

/**
 * Интерфейс для операций чтения данных об играх
 *
 * <p>Определяет контракт для классов, отвечающих за получение
 * информации об играх из различных источников данных.</p>
 *
 * @author Чекрыгин И.И.
 * @version 1.0
 */
public interface GameReader {

    /**
     * Возвращает список всех игр из коллекции
     *
     * @return список всех игр
     */
    List<Game> getAllGames();

    /**
     * Находит игру по уникальному идентификатору
     *
     * @param id идентификатор игры для поиска
     * @return найденная игра или null если игра не найдена
     */
    Game getGameById(int id);

    /**
     * Выполняет поиск игр по запросу
     *
     * <p>Поиск осуществляется по названию игры и жанру</p>
     *
     * @param query поисковый запрос
     * @return список игр, соответствующих запросу
     */
    List<Game> searchGames(String query);

    /**
     * Возвращает игры указанного жанра, подходящие для заданного количества игроков
     *
     * @param genre жанр для фильтрации
     * @param players количество игроков
     * @return список игр, отсортированный по убыванию максимального количества игроков
     */
    List<Game> getGamesByGenreAndPlayers(String genre, int players);
}